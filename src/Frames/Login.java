    private void login(){
        Connection con = null;
        con = cone.getConexion(); //trae la conexion
        try{
            ps = con.prepareStatement("SELECT * FROM especialista where usuario = ? and contrase単a = ?");
            ps.setString(1, textUsuario.getText());
            ps.setString(2, textPassword.getText());
  
            rs = ps.executeQuery(); // guarda el resutado de la consulta en res
            
            if(rs.next()){ 
                especialista = new Especialista(rs.getInt("idEspecialista"), rs.getString("nombre"), rs.getString("ap_paterno"), rs.getString("ap_materno"), rs.getString("cedula"), rs.getString("sexo"), rs.getInt("edad"), rs.getString("usuario"), rs.getString("contrase単a"));                
                
                BuscarPaciente buscarP = new BuscarPaciente(especialista);
                buscarP.setVisible(true);
                dispose();
 
            }else{
                JOptionPane.showMessageDialog(null,"Usuario o contrase単a incorrecta, verifique sus datos");
            }
            ps.close();
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, "Error al conectar Haciendo la consulta");
        }
    }
    
    private void validacionCamposVacios(){
        if(textUsuario.getText().isEmpty()  || textPassword.getText().isEmpty() ){
            JOptionPane.showMessageDialog(null, "Debe llenar los campos solicitados");
        }else
        login();
    }
    
